// src/app/layout.tsx (server component)

import type { Metadata } from "next";
import "./globals.css";
import { Toaster } from "sonner";
import ClientProviders from "./AuthProviderClient";

export const metadata: Metadata = {
  title: "Desco",
  description: "Generated by create next app",
  icons: {
    icon: [{ url: "/images/logo/web-logo.png", type: "image/png" }],
    apple: [{ url: "/images/logo/web-logo.png" }],
  },
};

export default function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  return (
    <html lang="en" suppressHydrationWarning>
      <head>
        <script
          dangerouslySetInnerHTML={{
            __html: `
              // Suppress UserValidationError from console
              (function() {
                const originalConsoleError = console.error;
                console.error = function(...args) {
                  const message = args.join(' ');
                  if (message.includes('UserValidationError') || 
                      message.includes('A user with this email address has already been registered')) {
                    return; // Suppress these specific errors
                  }
                  originalConsoleError.apply(console, args);
                };
              })();
            `,
          }}
        />
      </head>
      <body>
        <ClientProviders>
          {children}
          <Toaster richColors />
        </ClientProviders>
      </body>
    </html>
  );
}
